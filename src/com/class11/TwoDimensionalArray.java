package com.class11;

public class TwoDimensionalArray {
	public static void main(String[] args) {
		// 2D Array Syntax (   Data_Type[][] Array_Name = new Data_Type[Row_Size][Column_Size];     )
		
		
		
		int[][] numbers= {
				{13,17,44,78,97},
				{-19,90,44,55,66,-77},
				{68,65,47,79,81}		
		};
		
		System.out.println(numbers[1][5]);
	}

}
