package com.class7;

public class WhileLoopPractice1 {
	public static void main(String[]args) {
	//Print value from 50 to 20
	
	int value=50;
	
	while(value>=20) {
		System.out.print(value+ " ");
		value--;
	}
	
	int odd=50;
	
	while(odd<=100) {
		
		if(odd%2!=0) {
			System.out.print(odd+" ");
		}
	}odd++;
	
	
}
	
}
